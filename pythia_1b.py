base_model_name = "EleutherAI/pythia-1b"

# Load tokenizer
tokenizer = AutoTokenizer.from_pretrained(base_model_name)

# Load base model
model = AutoModelForCausalLM.from_pretrained(
    base_model_name,
    dtype=torch.float16,
    device_map="auto"
)

# Load LoRA adapter using absolute path
adapter_path = os.path.abspath("pythia1b-dolly-lora-adapter")
model = PeftModel.from_pretrained(model, adapter_path)

# Inference function with your updated generation settings
def generate(prompt, max_new_tokens=200):
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    output = model.generate(
        **inputs,
        max_new_tokens=max_new_tokens,
        do_sample=True,
        temperature=1.2,
        top_p=0.85,
        top_k=50,
        repetition_penalty=1.15,
    )
    return tokenizer.decode(output[0], skip_special_tokens=True)

# Test it — this prompt forces a new question + answer each run
