# CELL 3 — Apply Formatting + Tokenization to Train / Val / Test

def format_dolly(example):
    """
    Converts Dolly-style instruction data into a single text field.
    Expected keys: 'instruction', 'context', 'response'
    """
    instruction = example.get("instruction", "")
    context = example.get("context", "")
    response = example.get("response", "")

    text = f"Instruction: {instruction}\n"
    if context:
        text += f"Context: {context}\n"
    text += f"Response: {response}"

    return {"text": text}


# Apply Dolly formatting (creates the "text" field)
train_dataset = train_dataset.map(format_dolly)
eval_dataset  = eval_dataset.map(format_dolly)
test_dataset  = test_dataset.map(format_dolly)


