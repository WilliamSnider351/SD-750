import math
from transformers import TrainerCallback

# ---- Callback: print validation PPL at end of each epoch ----
class EpochSummaryCallback(TrainerCallback):
    def on_evaluate(self, args, state, control, metrics, **kwargs):
        # This is called immediately after each evaluation pass
        eval_loss = metrics.get("eval_loss", None)

        print("\n==============================")
        print(f" Epoch {int(state.epoch)} Validation Summary")
        print("==============================")

        if eval_loss is not None:
            eval_ppl = math.exp(eval_loss)
            print(f"Validation Loss: {eval_loss:.4f}")
            print(f"Validation Perplexity: {eval_ppl:.4f}")
        else:
            print("No eval_loss found in metrics.")

        print("==============================\n")


# ---- IMPORTANT: avoid OOM and fp16 gradient-scaler errors ----
training_args.per_device_eval_batch_size = 1
training_args.fp16 = False
training_args.bf16 = True
training_args.fp16_full_eval = False
training_args.bf16_full_eval = True


# ---- Trainer Setup ----
trainer = SFTTrainer(
    model=model,
    train_dataset=train_dataset,
    eval_dataset=eval_dataset,
    args=training_args,
    callbacks=[EpochSummaryCallback()],
)

# ---- Train ----
trainer.train()

# ---- Final Test Evaluation ----
